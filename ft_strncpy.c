#include "libft.h"

char	*ft_strncpy(char *d, const char *s, size_t len)
{
	size_t i;

	i = 0;
	while (s[i] && i < len && s[i] != '\0')
	{
		d[i] = s[i];
		i++;
	}
	while (i < len)
	{
		d[i] = '\0';
		i++;
	}
	return (d);
}
